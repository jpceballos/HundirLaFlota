    public class BtnAceptar implements ActionListener{
        @Override
        public void actionPerformed(ActionEvent arg0) {
            String nombre = jTextField1.getText();
            String password = new String(jPasswordField1.getPassword());
            boolean loginCorrecto = false;
                if (nombre.length()==0 || password.length()==0){
                    jLabel5.setText("DEBES INTRODUCIR LOS DATOS");
                    jLabel5.setVisible(true);
                }
                else{
                    try{
                        loginCorrecto = loginCorrecto = comprobarLogin(nombre, password); 
                            
                        if(loginCorrecto){
                            jLabel5.setVisible(false);
                            dispose();
                            Principal principal = new Principal();
                        }else{
                        jLabel5.setText("CREDENCIALES INCORRECTAS");
                        jLabel5.setVisible(true);
                        }
                    }
                    catch(Exception e){
                    }
                }   
        }
        
        private boolean comprobarLogin(String nombre, String password) throws Exception { try ( // metodo mediente el cual comprobamos que los datos introducidos concuerdan con los datos del archivo
            FileReader fr = new FileReader("datos.txt")) {
                BufferedReader br = new BufferedReader(fr);
                String s;
                while((s=br.readLine()) != null){
                    if(s.equals(nombre + " " + password)){
                        fr.close();
                        return true;
                    }
                }
            }
            return false;
	}   
    }
    
    

